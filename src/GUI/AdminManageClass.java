    public void RefreshSubjectComboBox() {

        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `subject`  ");
            Vector v = new Vector();

            while (rs.next()) {
                v.add(rs.getString(2));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel(model);
            jComboBox2.setModel(model);

        } catch (Exception e) {
        }
    }

    public void RefreshTeacherTable() {
        try {
            ResultSet result = MySQL.execute("SELECT * FROM `teacher`");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (result.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(result.getString("email"));
                vector.add(result.getString("fname") + " " + result.getString("fname"));
                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

