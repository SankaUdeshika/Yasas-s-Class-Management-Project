    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add New Subject
        String SubjectName = jTextField1.getText();

        if (SubjectName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Subject Name");
        } else {
            UUID uuid = UUID.randomUUID();
            String uniqueID = uuid.toString();

            MySQL.execute("INSERT INTO `subject` (`Subject_id`,`Subject_name`) VALUES ('" + uniqueID + "','" + SubjectName + "') ");
            RefreshSubjectComboBox();
            JOptionPane.showMessageDialog(this, "Subject Added");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        //Delete Subject
        String Subject = String.valueOf(jComboBox1.getSelectedItem());
        MySQL.execute("DELETE FROM `subject` WHERE `Subject_name` = '" + Subject + "'");
        JOptionPane.showMessageDialog(this, "Delete Success");
        RefreshSubjectComboBox();
        RefreshTeacherTable();
        RefreshTextField();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

