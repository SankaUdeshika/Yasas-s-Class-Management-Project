    public void RefreshGradeComboBox() {
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `grade`  ");
            Vector v = new Vector();

            while (rs.next()) {
                v.add(rs.getString(2));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox3.setModel(model);
            jComboBox4.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

